/**
 * Compilation: javac Comparison.java
 * Execution: java Comparison
 */

public class Comparison {
     public static void main(String[] args) {
         int value1 = 1;
         int value2 = 3;
         if (value1 == value2) {
             System.out.println("Value 1 equals value 2"); 
         } else {
             System.out.println("Value 1 does NOT equal value 2");
         }
     }
 }